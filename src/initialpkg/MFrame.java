package initialpkg;

import java.awt.*;
import javax.swing.*;

public class MFrame {
	
	public MFrame() {
		JFrame frame = new JFrame();
	}
	
	public MFrame(GraphicsConfiguration gc) {
		JFrame frame = new JFrame(gc);
	}
	
	public MFrame(String title){
		JFrame frame = new JFrame(title);
	}
	
	public MFrame(String title, GraphicsConfiguration gc){
		JFrame frame = new JFrame(title, gc);
	}
	
	public MFrame(String title, int sizeX, int sizeY){
		JFrame frame = new JFrame(title);
		frame.setSize(sizeX, sizeY);
	}
	
	public MFrame(String title, LayoutManager lm){
		JFrame frame = new JFrame(title);
		frame.setLayout(lm);
	}
}
